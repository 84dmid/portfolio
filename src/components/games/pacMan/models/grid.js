class Grid {
    constructor(game) {
        this.marking = initField;
        this.cellSize = 20;
        this.colsNum = this.marking[0].length;
        this.rowsNum = this.marking.length;
        this.height = this.cellSize * this.rowsNum;
        this.width = this.cellSize * this.colsNum;

        this.dotsNumber = 0;
        this.map = this.createMap();
    }

    createMap() {
        const result = [];

        let portal;

        this.marking.forEach((row, rowInd) => {
            result[rowInd] = [];
            row.forEach((cellType, colInd) => {
                const cell = {
                    x: colInd * this.cellSize + this.cellSize / 2,
                    y: rowInd * this.cellSize + this.cellSize / 2,
                    type: cellType,
                    rowInd,
                    colInd,
                };
                switch (cellType) {
                    case Game.CELL_TYPE.LITTLE_DOT:
                    case Game.CELL_TYPE.BIG_DOT:
                        this.dotsNumber++;
                        break;
                    case Game.CELL_TYPE.PORTAL:
                        if (portal) {
                            portal.exitPortal = cell;
                            cell.exitPortal = portal;
                        } else {
                            portal = cell;
                        }
                        this.exitPoint = this.leftPortal;
                        this.rightPortal = cell;
                        break;
                }

                result[rowInd][colInd] = cell;
            });
        });
        return result;
    }

    getCellByCoords(x, y) {
        let rowInd = Math.floor(y / this.cellSize);
        let colInd = Math.floor(x / this.cellSize);
        return this.map[rowInd][colInd];
    }

    checkDirection(x, y, direction, isGhostHomeOpened) {
        const checkedX = x + direction.x * this.cellSize;
        const checkedY = y + direction.y * this.cellSize;

        const checkedCell = this.getCellByCoords(checkedX, checkedY);

        if (checkedCell.type === Game.CELL_TYPE.GHOST_HOME_DOOR) {
            return isGhostHomeOpened;
        }
        if (checkedCell.type === Game.CELL_TYPE.WALL) {
            return false;
        }
        return true;
    }
}

export default Grid;
